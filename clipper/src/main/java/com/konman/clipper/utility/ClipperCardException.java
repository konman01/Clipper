package com.konman.clipper.utility;

public class ClipperCardException extends RuntimeException {

	public ClipperCardException() {
		super();
		
	}

	public ClipperCardException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public ClipperCardException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public ClipperCardException(String message) {
		super(message);
		
	}

	public ClipperCardException(Throwable cause) {
		super(cause);
	}

}
